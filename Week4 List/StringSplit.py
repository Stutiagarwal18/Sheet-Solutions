#15. Find the output :

s = "Hello everyone how are you"
print(s.split())    # ['Hello', 'everyone', 'how', 'are', 'you']

s = "Hello-everyone-how are you"
print(s.split("-")) # ['Hello', 'everyone', 'how are you']

word = 'Stuti agarwal:mathura'
print(word.split(':'))  # ['Stuti', 'agarwal', 'mathura']

t = "23456"
print(t.split())  # ['23456']

t = "2 3 4 5"
print(t.split())  # ['2','3','4','5']

